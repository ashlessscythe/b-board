"use client";

import * as React from "react";
import { useTheme } from "next-themes";

export function ThemeToggle() {
  const { theme, setTheme } = useTheme();

  return (
    <button
      onClick={() => setTheme(theme === "light" ? "dark" : "light")}
      className="relative inline-flex h-9 w-9 items-center justify-center rounded-lg bg-background hover:bg-accent transition-colors"
      aria-label="Toggle theme"
    >
      <div className="relative h-5 w-5">
        <span className="absolute inset-0 rotate-90 transform transition-transform dark:rotate-0">
          <Moon className="h-5 w-5" />
        </span>
        <span className="absolute inset-0 rotate-0 transform transition-transform dark:-rotate-90">
          <Sun className="h-5 w-5" />
        </span>
      </div>
    </button>
  );
}

